"""ComplyAgent — Streamlit dashboard.

Reads the most recent eval artifact at outputs/latest-eval.json and renders a
multi-tab demo. By design this dashboard does not hit the Gemini API; it
displays the most recent pre-computed eval, which is regenerated by
`uv run python scripts/run_eval.py <tenant>` and committed to the repo.

That choice is deliberate: a public demo URL where every visitor triggered
fresh classification would burn the shared free-tier quota in minutes and
make eval numbers non-deterministic.
"""
from __future__ import annotations

