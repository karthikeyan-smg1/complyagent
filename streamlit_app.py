with tab_inbox:
    st.markdown("## Bulletin inbox")
    st.markdown(
        "Every bulletin the agent has classified for this tenant. Filter by "
        "predicted relevance; click a row to expand reasoning and tags."
    )

    rows: list[dict] = []
    for r in results:
        tags = r.get("tags") or {}
        rel = r.get("relevance") or {}
        rows.append({
            "bulletin_id": r["bulletin_id"],
            "title": r.get("title", ""),
            "network": tags.get("network", "—"),
            "topic": tags.get("topic", "—"),
            "action_type": tags.get("action_type", "—"),
            "mandatory": "yes" if tags.get("mandatory") else "no",
            "effective_date": tags.get("effective_date") or "—",
            "predicted_relevant": rel.get("relevant"),
            "expected_relevant": r.get("expected_relevance"),
            "confidence": rel.get("confidence"),
            "affected_surfaces": ", ".join(rel.get("affected_surfaces", [])) or "—",
            "error": r.get("error", ""),
        })
    df = pd.DataFrame(rows)

    col_f1, col_f2, col_f3 = st.columns([1, 1, 2])
    with col_f1:
        filter_pred = st.selectbox(
            "Predicted",
            ["All", "Relevant only", "Not relevant only"],
            index=0,
        )
    with col_f2:
        filter_net = st.selectbox(
            "Network",
            ["All"] + sorted(df["network"].dropna().unique().tolist()),
            index=0,
        )
    with col_f3:
        search = st.text_input("Search title or ID", value="").strip().lower()

    view = df.copy()
    if filter_pred == "Relevant only":
        view = view[view["predicted_relevant"].astype(bool)]
    elif filter_pred == "Not relevant only":
        view = view[~view["predicted_relevant"].astype(bool)]
    if filter_net != "All":
        view = view[view["network"] == filter_net]
    if search:
        view = view[
            view["title"].str.lower().str.contains(search, na=False)
            | view["bulletin_id"].str.lower().str.contains(search, na=False)
        ]

    show_df = view[[
        "bulletin_id", "title", "network", "topic", "action_type",
        "mandatory", "predicted_relevant", "confidence", "affected_surfaces",
    ]].rename(columns={
        "predicted_relevant": "Predicted relevant",
        "confidence": "Confidence",
        "affected_surfaces": "Affected surfaces",
    })

    st.dataframe(
        show_df,
        hide_index=True,
        use_container_width=True,
        column_config={
            "Predicted relevant": st.column_config.CheckboxColumn(),
            "Confidence": st.column_config.ProgressColumn(min_value=0, max_value=1, format="%.2f"),
        },
    )

    st.markdown("### Bulletin detail")
    if len(view):
        selected_id = st.selectbox(
            "Inspect a bulletin",
            view["bulletin_id"].tolist(),
            index=0,
        )
        rec = next((r for r in results if r["bulletin_id"] == selected_id), None)
        if rec:
            c1, c2 = st.columns([2, 3])
            with c1:
                tags = rec.get("tags") or {}
                rel = rec.get("relevance") or {}
                st.markdown(f"**{rec.get('title', selected_id)}**")
                st.caption(f"`{selected_id}` · {rec.get('file', '')}")
                st.markdown(
                    f"- **Network:** {tags.get('network', '—')}\n"
                    f"- **Topic:** {tags.get('topic', '—')}\n"
                    f"- **Action type:** {tags.get('action_type', '—')}\n"
                    f"- **Mandatory:** {'yes' if tags.get('mandatory') else 'no'}\n"
                    f"- **Effective date:** {tags.get('effective_date') or '—'}\n"
                    f"- **Predicted relevant:** {rel.get('relevant')} "
                    f"(confidence {rel.get('confidence', 0):.2f})\n"
                    f"- **Expected (ground truth):** {rec.get('expected_relevance') or '—'} "
                    f"({rec.get('expected_priority') or '—'})\n"
                    f"- **Affected surfaces:** "
                    f"{', '.join(rel.get('affected_surfaces', [])) or '—'}"
                )
                if rec.get("error"):
                    st.error(rec["error"])
            with c2:
                st.markdown("**Stage-2 reasoning**")
                reasoning = rec.get("relevance", {}).get("reasoning", "—")
                st.info(reasoning)

                bulletin_path = PROJECT_ROOT / rec.get("file", "")
                if bulletin_path.exists():
                    with st.expander("View bulletin source"):
                        st.markdown(load_text(bulletin_path))


